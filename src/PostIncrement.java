
public class PostIncrement {

	public static void main(String[] args) {
		
		int a =1;
		int b =a++;
		System.out.println(b);
		System.out.println(a); 
		
		int l =3;
		int m=l++;
		System.out.println(l);
		System.out.println(m);
		
		
		// preincrement
		int u=1;
		int y=++u;
		System.out.println(y);
		
		
		
		
	}
	
	
	
}
